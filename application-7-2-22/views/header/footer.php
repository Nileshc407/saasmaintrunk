function delete_me(arg1, arg2, arg3, argUrl) 
{

    $(".modal-body #arg1").val(arg1);
    $(".modal-body #arg1").html(arg1);

    $(".modal-body #arg2").val(arg2);
    $(".modal-body #arg2").html(arg2);


    $(".modal-body #arg3").val(arg3);
	if(arg3 != "")
	{
		$(".modal-body #arg3").html('Warning : '+arg3+'!!');
	}
    $(".modal-body #argUrl").val(argUrl);

}
/*
 arg1= For Running Id
 arg2= For Name which will be deleted
 arg3= For Extra Information want to show on Popup
 argUrl= Deleted URL  
   
   
  */
function confirmed_delete(arg1, arg2, arg3, argUrl) {

    if (argUrl)
    {
        var url = '<?php echo base_url(); ?>index.php/' + argUrl + '=' + arg1;
        $('#deleteModal').modal('hide');
        show_loader();
        setTimeout(function () {
            window.location = url;
        }, 3000)


        return true;
    } else
    {
        return false;
    }

}
